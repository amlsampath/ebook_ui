class BookModel {
  String title;
  String rating;
  String description;
  String price;
  String image;
  String views;
  String author;
  String followers;
  String following;
  BookModel({
    required this.description,
    required this.price,
    required this.rating,
    required this.title,
    required this.image,
    required this.author,
    required this.followers,
    required this.following,
    required this.views,
  });
}
